#pragma mark - Table view delegate

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    AC_ApplicationDetailViewController* detailViewController = [[AC_ApplicationDetailViewController alloc] initWithNibName:@"AC_ApplicationDetailViewController" bundle:nil];
    AM_Application* application = [applications objectAtIndex:indexPath.row];
    [detailViewController setApplication:application];
    [self.navigationController pushViewController:detailViewController animated:YES];
    [detailViewController release];
    
}

